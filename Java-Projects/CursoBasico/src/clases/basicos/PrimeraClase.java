package clases.basicos;

public class PrimeraClase {
	  
	public static void main(String args[]) {
		System.out.println("Hola Mundo");
		//Tipos de datos PRIMITIVOS
			//Enteros
				//Byte 1 byte   (-128 - 127)
				//Short 2 bytes (|32768|)
				//Int 4 bytes
				//Long 8 bytes  - sufijo L   -> 5000000000L
			//Decimales
				//Float 4 bytes 6 a 7 decimales -> 3,25F
				//Double 8 bytes hasta 15 decimales -> 5,42423423423523523523 sin sufijo
			//Char caracteres - van con comilla simple
			//Boolean
		for (int i = 0; i < 500000; i++) {
			//vacio
			
			System.out.println("hola " + i);
		}
	}
}
